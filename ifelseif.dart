void main(){
  //if-elseif-else statement allows execution of multiple criteria
  var age = 18;
  if (age > 18){
    print("You are eligible to vote in Nigeria");
  }
  else if (age == 18){
    print("Congratulations, you just became eligible to vote in Nigeria");
  }
  else{
    print("You are NOT eligible to vote in Nigeria");
  }
}