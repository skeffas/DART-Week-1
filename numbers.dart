void main() {
  //Working with numbers in dart
  int num1 = 100; //int variable used to store whole numbers
  double num2 = 5.7; //double variable is used to store decimal numbers
  print(num1);
  print(num2);

  var sum = num1 + num2;
  var multiply = num1 * num2;
  var divide = num1 / num2;

  print(sum);
  print(multiply);
  print(divide);
}